#include "3-calc.h"
#include <stdio.h>
#include <stdlib.h>

/**
 * op_add - to add int
 * @a: first int
 * @b: second int
 * Return: add
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - to subtrct int
 * @a: first int
 * @b: second int
 * Return: add
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply int
 * @a: first int
 * @b: second int
 * Return: add
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - to divide int
 * @a: first int
 * @b: second int
 * Return: add
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulo of int
 * @a: first int
 * @b: second int
 * Return: add
 */
int op_mod(int a, int b)
{
	return (a % b);
}
